# 5-year 2015 =================================================================
#' ACS 5-year 2015 file segment and table lookup data
#'
#' @docType data
#'
#' @format A data.table with 4 variables
#' \describe{
#'   \item{file_segment}{sequence number of segment data files, from 1 to 166}
#'   \item{table_content}{description of content in a census table}
#'   \item{reference}{reference of the field, such as "B01001_002"}
#'   \item{table_number}{table number such as "B01001"}
#'   \item{table_name}{description of table, which has many table_content}
#'   \item{universe}{the unit of the census data}
#' }
#'
#' @keywords datasets
#'
#' @source Check for each year of ACS 1-year and 5-year
#' \href{https://www.census.gov/programs-surveys/acs/technical-documentation/summary-file-documentation.2015.html}{Sequence Number/Table Number Lookup File}.
#'

"lookup_acs5year_2015"


